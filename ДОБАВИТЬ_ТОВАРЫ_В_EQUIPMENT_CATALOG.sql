-- Показываем добавленные товары
SELECT 
    name, 
    price_per_day, 
    stock_quantity,
    created_at
FROM equipment_catalog 
WHERE is_active = true 
ORDER BY created_at DESC 
LIMIT 10;

-- Группируем по цене для анализа
SELECT 
    CASE 
        WHEN price_per_day < 10000 THEN 'До 10,000₽'
        WHEN price_per_day < 50000 THEN '10,000-50,000₽'
        ELSE 'Свыше 50,000₽'
    END as price_range,
    COUNT(*) as count
FROM equipment_catalog 
WHERE is_active = true
GROUP BY 
    CASE 
        WHEN price_per_day < 10000 THEN 'До 10,000₽'
        WHEN price_per_day < 50000 THEN '10,000-50,000₽'
        ELSE 'Свыше 50,000₽'
    END
ORDER BY count DESC;
